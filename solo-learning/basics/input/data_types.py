#bmi formula = height / weight x2

print("What is your name dude?")
name = str(input())
print("How old are you?(in years)")
age = int(input())
print("How tall are you?(in meters)")
height = float(input())
print("What's your weight?(in kg)")
weight = float(input())

bmi = float
bmi = weight / height**2
print("{} you are {} years old and your bmi is {}.".format(name,age,bmi))

if bmi <= 18.5:
  print("You are damn skinny. get some meat!")
elif bmi <= 24.9:
  print("healthy. good boy, you deserve a bone!")
elif bmi <= 29.9:
  print("overweight cow. go exercise!")
elif bmi >= 30:
  print("over the limit MUUUUUUUU")

  



      
    

